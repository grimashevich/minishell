#include "parser.h"

void	print_end(void)
{
	if (PARSER_DEBAG == 1)
	{
		printf("\x1b[42m+------------------------------------------------------------------------------+\x1b[0m\n");
		printf("\x1b[42m|                                                                              |\x1b[0m\n");
		printf("\x1b[42m+------------------------------------------------------------------------------+\x1b[0m\n");
	}
}

void	print_result(char *line)
{
	if (PARSER_DEBAG == 1)
	{
		printf("\x1b[32mResult:\x1b[0m\n");
		printf("+------------------------------------------------------------------------------+\n");
		printf("| %-77s|\n", line);
		printf("+------------------------------------------------------------------------------+\n");
	}
}

void	print_this_is_something(char *line, char *type)
{
	if (PARSER_DEBAG == 1)
	{
		printf("\x1b[33mThis is: \x1b[32m%s\x1b[0m\n", type);
		printf("+------------------------------------------------------------------------------+\n");
		printf("| %-77s|\n", line);
		printf("+------------------------------------------------------------------------------+\n");
	}
}

void	print_last_character(int character)
{
	if (PARSER_DEBAG == 1)
	{
		printf("\x1b[33mLast character:\x1b[0m\n");
		printf("+------------------------------------------------------------------------------+\n");
		printf("| %-77d|\n", character);
		printf("+------------------------------------------------------------------------------+\n");
	}
}

void	print_substr(char *line, int start, int lenght)
{
	if (PARSER_DEBAG == 1)
	{
		printf("\x1b[33mStart:\x1b[0m\n");
		printf("+------------------------------------------------------------------------------+\n");
		printf("| %-77d|\n", start);
		printf("+------------------------------------------------------------------------------+\n");
		printf("\x1b[33mLenght:\x1b[0m\n");
		printf("+------------------------------------------------------------------------------+\n");
		printf("| %-77d|\n", lenght - start);
		printf("+------------------------------------------------------------------------------+\n");
		printf("\x1b[33mRemove insignificant spases:\x1b[0m\n");
		printf("+------------------------------------------------------------------------------+\n");
		printf("| %-77s|\n", line);
		printf("+------------------------------------------------------------------------------+\n");
	}
}

